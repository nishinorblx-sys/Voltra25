local function A(x, y, z)
	return CFrame.Angles(math.rad(x), math.rad(y), math.rad(z))
end

local function KF(time, rootJoint, neck, rightShoulder, leftShoulder, rightHip, leftHip, extra)
	local keyframe = {
		Time = time,
		RootJoint = rootJoint,
		Neck = neck,
		RightShoulder = rightShoulder,
		LeftShoulder = leftShoulder,
		RightHip = rightHip,
		LeftHip = leftHip,
	}
	if extra then
		for key, value in pairs(extra) do
			keyframe[key] = value
		end
	end
	return keyframe
end

