local byName = {}
for _, celebration in ipairs(Celebrations) do
	byName[string.lower(celebration.Name)] = celebration
end

function Config.Resolve(idOrName)
	local key = tostring(idOrName or "")
	local mappedName = idToName[key]
	if mappedName and byName[string.lower(mappedName)] then
		return byName[string.lower(mappedName)]
	end
	local direct = byName[string.lower(key)]
	if direct then
		return direct
	end
	return byName.wave or Celebrations[1]
end

return Config

